// Function to change language
function changeLanguage() {
    // Get the language selector element and selected language
    const languageSelector = document.getElementById('language-selector');
    const selectedLanguage = languageSelector.value;

    // Get all elements with an id attribute
    const elements = document.querySelectorAll('[id]');

    // Iterate through all elements with ids
    elements.forEach(element => {
        const elementId = element.id;
        console.log(elementId)
        // Check if translation exists for this element id and selected language
        if (translations[selectedLanguage] && translations[selectedLanguage][elementId]) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                // For input and textarea elements, update placeholder
                if (element.placeholder) {
                    element.placeholder = translations[selectedLanguage][elementId];
                }
            } else if (element.tagName === 'IMG') {
                // For image elements, update alt text
                element.alt = translations[selectedLanguage][elementId];
            } else {
                // For other elements, update innerHTML
                element.innerHTML = translations[selectedLanguage][elementId];
            }
        }
    });

    // Save selected language to localStorage for persistence
    localStorage.setItem('selectedLanguage', selectedLanguage);
}

// Set language on page load
document.addEventListener('DOMContentLoaded', function () {
    // Retrieve saved language from localStorage or default to 'en'
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    const languageSelector = document.getElementById('language-selector');

    // Set the language selector to the saved language and trigger language change
    if (languageSelector) {
        languageSelector.value = savedLanguage;
        changeLanguage();
    }
});
